public class Calculator {

    public static double getSuma(double a, double b) {
        return a +  b;
    }

    public static double incrementa(double a) {
        return a + 1;
    }

    public static double decrementa(double a) {
        return a - 1;
    }

    public static double getDiferencia(double a, double b) {
        return a - b;
    }
}